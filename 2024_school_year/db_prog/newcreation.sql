-- Create a table to store long strings for overview, capabilities, deployment, and history
CREATE TABLE MechDescriptions (
    description_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    variant_id NUMBER,
    overview CLOB,
    capabilities CLOB,
    deployment CLOB,
